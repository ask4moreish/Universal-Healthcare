export interface Notification {
  id: string
  recipientId: string
  actorId: string | null
  type: 'follow' | 'comment_reply'
  entityType: 'follow' | 'comment'
  entityId: string
  read: boolean
  createdAt: Date
  updatedAt: Date
}

